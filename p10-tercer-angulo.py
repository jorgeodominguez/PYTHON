#Dados dos ángulos de un triángulo, calcular el 3er ángulo.

print("Dados dos ángulos de un triángulo, calcular el 3er ángulo,")

angulo1=float(input("\nIngresa angulo 1: "))
angulo2=float(input("Ingresa angulo 2: "))

angulo3=180-(angulo1+angulo2)

print(f"\nEl angulo del 3 es: {angulo3}"
        f"\ndando el angulo 1: {angulo1}, angulo 2: {angulo2}\n")