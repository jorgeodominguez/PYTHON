#Dada una temperatura en grados celcius, obtener su equivalente en grados farenheit.

print("Dada una temperatura en grados celcius, obtener su equivalente en grados farenheit")

celcius=float(input("\nIngresa los grados Celcius: "))

farenheit = (celcius * 9/5) + 32

print(f"\nLa temperatura en Celciuos: {celcius} a Farenheit es: {farenheit}\n")